public class typesOfLoops {
    public static void main(String[] args){
//        There are mainly four types of type of loops
//            1. For loop
//            2. While loop
//            3. do while loop

        for(int i =0;i<10;i++){
            System.out.print(i + " ");
        }
        System.out.println(" ");

        int i =0;
        while(i<10){
            System.out.print(i++ + " ");
        }
    }
}
